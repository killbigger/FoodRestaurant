class FoodTypeResponse {
  String name;
  int id;

  FoodTypeResponse({this.name, this.id});

  FoodTypeResponse.fromJson(Map<String, dynamic> json) {
    name = json['name'];
    id = json['id'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['name'] = this.name;
    data['id'] = this.id;
    return data;
  }
}

